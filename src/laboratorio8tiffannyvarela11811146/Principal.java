    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        try {
            
            String c=txt_cant.getText();
            int alt=0,tam=c.length();
            int exitosos=0,fallidos=0;
            int pos = jList2.getSelectedIndex();
            DefaultListModel modelo = (DefaultListModel) jList1.getModel();
            ((Hackeo)modelo.get(pos)).setE(3);
            
            Hackeo h = ((Hackeo)modelo.get(pos));
             if (((Hackeo) modelo.get(pos)).getTipo().equals("Regular")) {
                alt=1+r.nextInt(9);
            }
            if (((Hackeo) modelo.get(pos)).getTipo().equals("Super")) {
                alt=1+r.nextInt(10);
                while(alt<6||alt>10){
                    alt=1+r.nextInt(10);
                }
            }
            int tipo = h.getNivel();
            int duracion=0;
            if (h.getTipo().equals("Regular")) {
                duracion=2;
            }
            else{
                duracion=1;
            }
            tiempo.setMaximum(duracion*tam);
            Hilo hilo = new Hilo(tiempo, duracion, tam);
            hilo.start();
            if (tam==((Hackeo)modelo.get(pos)).getNivel()) {
                hilo.stop();
                
                if (((Hackeo)modelo.get(pos)).getTipo().equals("Super")) {
                    fallidos++;
                }
            }
            if (((Hackeo)modelo.get(pos)).getTipo().equals("Regular")) {
                    exitosos++;
                }
            
            lbl_exitosos.setText(Integer.toString(exitosos));
            lbl_fallidos.setText(Integer.toString(fallidos));
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error");
        }

    }//GEN-LAST:event_jButton6ActionPerformed

