    private void cmb_tipoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmb_tipoItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == 2) {
            if (cmb_tipo.getSelectedItem().toString().equals("Regular")) {
                spinner_regular.setEnabled(true);
                spinner_super.setEnabled(false);
            }
            if (cmb_tipo.getSelectedItem().toString().equals("Super")) {
                spinner_regular.setEnabled(false);
                spinner_super.setEnabled(true);
            }
        }

    }//GEN-LAST:event_cmb_tipoItemStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int exito = 0, exi = 0;
            DefaultListModel modelo = (DefaultListModel) jList1.getModel();
            if (cmb_tipo.getSelectedItem().toString().equals("Regular")) {
                modelo.addElement(new Regular(exito, txt_cod.getText(), (Integer) spinner_regular.getValue(), exi, cmb_tipo.getSelectedItem().toString()));
                jList1.setModel(modelo);
jList2.setModel(modelo);
                txt_cod.setText("");
                spinner_regular.setValue(1);
                cmb_tipo.setSelectedIndex(0);
                spinner_regular.setEnabled(false);
                spinner_super.setEnabled(false);
            }
            if (cmb_tipo.getSelectedItem().toString().equals("Super")) {
                modelo.addElement(new Super(exito, txt_cod.getText(), (Integer) spinner_super.getValue(), exi, cmb_tipo.getSelectedItem().toString()));
                jList1.setModel(modelo);
                jList2.setModel(modelo);
                spinner_regular.setEnabled(false);
                spinner_super.setEnabled(false);
                txt_cod.setText("");
                spinner_super.setValue(6);
                cmb_tipo.setSelectedIndex(0);
            }
            JOptionPane.showMessageDialog(this, "Guardado correctamente");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            int pos = jList1.getSelectedIndex();
            DefaultListModel modelo = (DefaultListModel) jList1.getModel();

            if (((Hackeo)modelo.get(pos)).getE()==0) {
                txt_cod.setText(((Hackeo) modelo.get(pos)).getCod());
            if (((Hackeo) modelo.get(pos)).getTipo().equals("Regular")) {
                cmb_tipo.setSelectedIndex(1);
                spinner_regular.setValue(((Hackeo) modelo.get(pos)).getNivel());
            }
            if (((Hackeo) modelo.get(pos)).getTipo().equals("Super")) {
                cmb_tipo.setSelectedIndex(2);
                spinner_super.setValue(((Hackeo) modelo.get(pos)).getNivel());
            }
            modelo.remove(pos);
            jList1.setModel(modelo);
            jList2.setModel(modelo);
            }
            
            if (((Hackeo)modelo.get(pos)).getE()==3) {
                JOptionPane.showMessageDialog(this, "Este metodo no se puede modificar");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

